void LoadComputerFromImage(Color *grid_pixels){
  Image computer = LoadImage("imgs/counting_cpu.png");
  Color *computercolors = LoadImageColors(computer);
  for(int i = 0; i < computer.width; i++){
    for(int j = 0; j < computer.height; j++){
	size_t cell_idx = j*computer.width + i;
	int thecolor = ColorToInt(computercolors[cell_idx]);
	switch(thecolor){
		case 0x000000FF:
			cell_grid[i][j] = EMPTY;
			grid_pixels[j*GRID_WIDTH + i] = state_colors[EMPTY];
			break;
		case 0xFFFFFFFF:
			cell_grid[i][j] = HEAD;
			grid_pixels[j*GRID_WIDTH + i] = state_colors[HEAD];
			break;
		case 0x0080FFFF: 
			cell_grid[i][j] = TAIL;
			grid_pixels[j*GRID_WIDTH + i] = state_colors[TAIL];
			break;
		case 0xFF8000FF:
			cell_grid[i][j] = WIRE;
			grid_pixels[j*GRID_WIDTH + i] = state_colors[WIRE];
			break;
	}
    }
  }
  UnloadImage(computer);
  UnloadImageColors(computercolors);


}




void clearCells(void) {
  for (int i = 0; i < GRID_WIDTH; i++) {
    for (int j = 0; j < GRID_HEIGHT; j++) {
      cell_grid[i][j] = EMPTY;
      next_cell_grid[i][j] = EMPTY;
    }
  }
  TraceLog(LOG_INFO, "Cleared cell grid that was size %dx%d and was %d bytes\n",
           GRID_WIDTH, GRID_HEIGHT,
           GRID_WIDTH * GRID_HEIGHT * sizeof(cell_state));
}

cell setCell(cell_coord coordinate, cell_state new_state) {
  if (coordinate.x >= 0 && coordinate.y >= 0 && coordinate.x < GRID_WIDTH &&
      coordinate.y < GRID_HEIGHT) {
    cell_grid[coordinate.x][coordinate.y] = new_state;
    cell retcell;
    retcell.coord = coordinate;
    retcell.state = new_state;
    return retcell;
  }
  cell err;
  err.state = EMPTY;
  err.coord.x = -1;
  err.coord.y = -1;
  return err;
}

cell_coord getCellIdx(Vector2 mouse_pos) {
  cell_coord cell_idx = {0, 0};
  cell_idx.x = mouse_pos.x / CELL_SIZE;
  cell_idx.y = mouse_pos.y / CELL_SIZE;
  // TraceLog(LOG_DEBUG, "x,y = (%d,%d)", cell_idx.x, cell_idx.y);
  return cell_idx;
}

cell_neighbors stateInMoore(int x, int y) {
  cell_neighbors neighbors = {0};

  // Check all 8 neighboring cells for the target state
  for (int i = x - 1; i <= x + 1; i++) {
    for (int j = y - 1; j <= y + 1; j++) {
      // Skip the cell itself
      if (i == x && j == y)
        continue;

      // Check if the neighboring cell is in bounds
      if (i >= 0 && i < GRID_WIDTH && j >= 0 && j < GRID_HEIGHT) {
        // Check if the neighboring cell has the target state
        neighbors.heads += cell_grid[i][j] == HEAD;
        neighbors.tails += cell_grid[i][j] == TAIL;
      }
    }
  }

  return neighbors;
}

void updateGrid(cell *changedCells, size_t *num_changed_coords) {
  /*
   * As in all cellular automata, time proceeds in discrete steps called
   * generations (sometimes gens or ticks). Cells behave as follows:
   *
   *     empty → empty,
   *     electron head → electron tail,
   *     electron tail → conductor,
   *     conductor → electron head if exactly one or two of the neighbouring
   * cells are electron heads, otherwise remains conductor.*/
  size_t cellidx = 0;
  for (size_t i = 0; i < GRID_WIDTH; i++) {
    for (size_t j = 0; j < GRID_HEIGHT; j++) {

      cell_neighbors neighbors;
      if (cell_grid[i][j] != EMPTY)
        neighbors = stateInMoore(i, j);

      if (cell_grid[i][j] == TAIL) {
        cell new_cell;
        new_cell.coord.x = i;
        new_cell.coord.y = j;
        new_cell.state = WIRE;
        changedCells[cellidx++] = new_cell;
      }
      if (cell_grid[i][j] == HEAD && neighbors.tails > 0) {
        cell new_cell;
        new_cell.coord.x = i;
        new_cell.coord.y = j;
        new_cell.state = TAIL;
        changedCells[cellidx++] = new_cell;
      }
      if (cell_grid[i][j] == WIRE &&
          (neighbors.heads == 1 || neighbors.heads == 2)) {
        cell new_cell;
        new_cell.coord.x = i;
        new_cell.coord.y = j;
        new_cell.state = HEAD;
        changedCells[cellidx++] = new_cell;
      }
    }
  }
  for(size_t i = 0; i < cellidx; i++){
     cell cur = changedCells[i];
     cell_grid[cur.coord.x][cur.coord.y] = cur.state;
  }
  *num_changed_coords += cellidx;
}

